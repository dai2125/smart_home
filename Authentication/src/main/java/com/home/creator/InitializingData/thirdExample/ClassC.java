package com.home.creator.InitializingData.thirdExample;

public class ClassC {

    private String text;
    private int num;

    public ClassC(String text, int num) {
        this.text = text;
        this.num = num;
    }

    public String getText() {
        return text;
    }

    public int getNum() {
        return num;
    }
}
